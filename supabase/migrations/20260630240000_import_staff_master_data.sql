  sr.employee_id,
  sr.full_name,
  sr.email,
  sr.phone_number,
  sr.address,
  sr.aadhaar,
  sr.pan,
  sr.role,
  sr.employee_type,
  sr.joining_date,
  sr.status,
  sr.degree,
  sr.institution,
  sr.year_passed,
  sr.account_holder_name
FROM seed_rows sr
WHERE NOT EXISTS (
  SELECT 1
  FROM public.employees e
  WHERE lower(trim(coalesce(e.email, ''))) = lower(trim(coalesce(sr.email, '')))
     OR (
       sr.aadhaar IS NOT NULL
       AND sr.aadhaar <> ''
       AND e.aadhaar IS NOT NULL
       AND e.aadhaar = sr.aadhaar
     )
);

